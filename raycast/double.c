#include "raycast.h"

double	d_abs(double num)
{
	if (num < 0)
		num *= -1;
	return (num);
}

double	comp_dot(t_p2 z1, t_p2 z2)
{
	return(z1.x * z2.x + z1.y * z2.y);
}
